# ARITHMATIC OPERATIONS

a=5
b=2

print(a+b) #7
print(a-b) #3
print(a*b) #10

print(a/b) #2.5
print(a//b) # 2 quotient
print(a%b) #1 reminder
print(a**b) #5**2=25 square


# RELATIONAL OPERATOR

# > < >= <= != ==
# relational operators always return boolean value
a= 5
b=10
print(a>b)  # False
print(a<b) # True
print(a==b) # False
print(a!=b) # True
print(a<=b)  # True
print(a>=b) # False


#LOGICAL OPERATOR

#  [ and, or,  not ]  ---> in Java &&, ||

#always returns boolean value True/False
a=True
b=False
print(a and b) # False
print(a or b) # True
print(not a) # False

